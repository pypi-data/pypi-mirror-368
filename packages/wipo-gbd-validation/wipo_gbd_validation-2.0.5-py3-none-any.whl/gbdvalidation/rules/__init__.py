from enum import Enum

class ErrorSeverity(Enum):
    CRITICAL = 'CRITICAL'
    ERROR = 'ERROR'
    WARNING = 'WARNING'
    INFO = 'INFO'
    DEBUG = 'DEBUG'

def get_error_from_code(error_code):
    from .codes import ERROR_TYPE, GROUP_TYPE
    error = {
        'code': error_code,
    }
    group = GROUP_TYPE.get(error_code[0:-1], None)
    if group is not None:
        error.update({'field': group})

    type = ERROR_TYPE.get(error_code[-1], None)
    if type is not None:
        error.update(type)
    error['message'] = "[%s][%s]%s" % (error['severity'].value, error['field'], error['type'])
    error['severity'] = error['severity'].value
    return error

